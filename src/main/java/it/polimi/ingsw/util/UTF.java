package it.polimi.ingsw.util;

/**
 * This class is a collection of UTF characters to be used when printing the CLI interface.
 */
public abstract class UTF {
    /**
     * A vertical wall.
     */
    public static final String vertical = "\u2551";

    /**
     * A horizontal wall.
     */
    public static final String horizontal = "\u2550";

    /**
     * A bottom-left corner.
     */
    public static final String corner1 = "\u255a";

    /**
     * A bottom-right corner.
     */
    public static final String corner2 = "\u255d";

    /**
     * A top-right corner.
     */
    public static final String corner3 = "\u2557";

    /**
     * A top-left corner.
     */
    public static final String corner4 = "\u2554";

    /**
     * A square.
     */
    public static final String block = "\u25a0";

    /**
     * A full rectangle.
     */
    public static final String full = "\u2588";

    /**
     * An empty rectangle.
     */
    public static final String empty = "\u2591";

    /**
     * A death symbol.
     */
    public static final String skull = "\u256c";
}
