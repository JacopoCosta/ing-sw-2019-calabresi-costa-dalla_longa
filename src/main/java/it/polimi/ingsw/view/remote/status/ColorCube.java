package it.polimi.ingsw.view.remote.status;

public class ColorCube {
    private int red;
    private int yellow;
    private int blue;

    public ColorCube(int red, int yellow, int blue) {
        this.red = red;
        this.yellow = yellow;
        this.blue = blue;
    }

    public int getRed() {
        return red;
    }

    public int getYellow() {
        return yellow;
    }

    public int getBlue() {
        return blue;
    }
}
