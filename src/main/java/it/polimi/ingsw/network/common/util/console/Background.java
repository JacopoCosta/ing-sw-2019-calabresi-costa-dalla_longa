package it.polimi.ingsw.network.common.util.console;

/**
 * This class contains all the possible background colors available via the ANSI escape characters.
 */
public class Background {
    /**
     * The reset ANSI escape {@code String}.
     */
    public static final String ANSI_RESET = "\u001B[0m";

    /**
     * The black background ANSI escape {@code String}.
     */
    public static final String ANSI_BLACK_BACKGROUND = "\u001B[40m";

    /**
     * The red background ANSI escape {@code String}.
     */
    public static final String ANSI_RED_BACKGROUND = "\u001B[41m";

    /**
     * The green background ANSI escape {@code String}.
     */
    public static final String ANSI_GREEN_BACKGROUND = "\u001B[42m";

    /**
     * The yellow background ANSI escape {@code String}.
     */
    public static final String ANSI_YELLOW_BACKGROUND = "\u001B[43m";

    /**
     * The blue background ANSI escape {@code String}.
     */
    public static final String ANSI_BLUE_BACKGROUND = "\u001B[44m";

    /**
     * The purple background ANSI escape {@code String}.
     */
    public static final String ANSI_PURPLE_BACKGROUND = "\u001B[45m";

    /**
     * The cyan background ANSI escape {@code String}.
     */
    public static final String ANSI_CYAN_BACKGROUND = "\u001B[46m";

    /**
     * The white background ANSI escape {@code String}.
     */
    public static final String ANSI_WHITE_BACKGROUND = "\u001B[47m";
}
